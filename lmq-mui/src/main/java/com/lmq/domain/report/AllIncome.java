package com.lmq.domain.report;

public class AllIncome {
	private Double xs;	//销售收入
	private Double dj;	//定金
	private Double cz;	//充值
	private Double jh;	//进货支出
	private Double fk;	//付款
	public Double getXs() {
		return xs;
	}
	public void setXs(Double xs) {
		this.xs = xs;
	}
	public Double getDj() {
		return dj;
	}
	public void setDj(Double dj) {
		this.dj = dj;
	}
	public Double getCz() {
		return cz;
	}
	public void setCz(Double cz) {
		this.cz = cz;
	}
	public Double getJh() {
		return jh;
	}
	public void setJh(Double jh) {
		this.jh = jh;
	}
	public Double getFk() {
		return fk;
	}
	public void setFk(Double fk) {
		this.fk = fk;
	}
	
}
